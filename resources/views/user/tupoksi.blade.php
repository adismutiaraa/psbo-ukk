		<p>3. Pengaturan Rambu-rambu lalulintas di dalam wilayah kampus IPB, baik berupa rambu-rambu peringatan maupun rambu-rambu larangan, melakukan teguran kepada pengendara sepeda motor dan tukang ojek agar mengatur knalpotnya supaya tidak bising.</p>
		<p>4. Pengaturan tanda pengenal.</p>
		<p>5. Pengaturan keluar masuknya semua jenis barang yang harus dilengkapi dengan surat pengantar dari unit kerja masing-masing atau surat jalan yang membolehkan mengangkut barang.</p>
